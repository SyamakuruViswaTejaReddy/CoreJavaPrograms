public class Datatypes {
    public static void main(String[] args) {
    // Primitive Data Types
    byte b = 100; // 8-bit signed integer           
    short s = 10000; // 16-bit signed integer
    int i = 100000; // 32-bit signed integer
    long l = 100000L; // 64-bit signed integer
    float f = 10.5f; // 32-bit floating point   
    double d = 20.99; // 64-bit floating point
    char c = 'A'; // 16-bit Unicode character       
    boolean bool = true; // true or false value
    // Non-Primitive Data Types
    String str = "Hello, World!"; // Sequence of characters
    Integer integerObject = 42; // Wrapper class for int
    Double doubleObject = 3.14; // Wrapper class for double
    }
}
