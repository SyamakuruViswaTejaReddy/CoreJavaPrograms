public class ClassObjectDemo {
    // This class demonstrates the use of classes and objects in Java
    public static void main(String[] args) {
        // Creating an instance of the ClassObjectDemo class
        ClassObjectDemo demo = new ClassObjectDemo();
        
        // Calling a method on the instance
        demo.displayMessage();
    }
    // A method that prints a message to the console
    public void displayMessage() {  
        System.out.println("Hello, this is a demonstration of classes and objects in Java!");
    }
}
